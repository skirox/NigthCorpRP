function OpenBodySearchMenu(player)



  ESX.TriggerServerCallback('esx_blanchiment:getOtherPlayerData', function(data)



    local elements = {}



    local blackMoney = 0



    for i=1, #data.accounts, 1 do

      if data.accounts[i].name == 'black_money' then

        blackMoney = data.accounts[i].money

      end

    end



    table.insert(elements, {

      label          = 'confisquer argent sale : $' .. blackMoney,

      value          = 'black_money',

      itemType       = 'item_account',

      amount         = blackMoney

    })



    table.insert(elements, {label = '--- Armes ---', value = nil})



    for i=1, #data.weapons, 1 do

      table.insert(elements, {

        label          = 'confisquer ' .. ESX.GetWeaponLabel(data.weapons[i].name),

        value          = data.weapons[i].name,

        itemType       = 'item_weapon',

        amount         = data.ammo,

      })

    end



    table.insert(elements, {label = '--- Inventaire ---', value = nil})



    for i=1, #data.inventory, 1 do

      if data.inventory[i].count > 0 then

        table.insert(elements, {

          label          = 'confisquer x' .. data.inventory[i].count .. ' ' .. data.inventory[i].label,

          value          = data.inventory[i].name,

          itemType       = 'item_standard',

          amount         = data.inventory[i].count,

        })

      end

    end





    ESX.UI.Menu.Open(

      'default', GetCurrentResourceName(), 'body_search',

      {

        title    = 'Faire les poches',

        align    = 'right',

        elements = elements,

      },

      function(data, menu)



        local itemType = data.current.itemType

        local itemName = data.current.value

        local amount   = data.current.amount



        if data.current.value ~= nil then



          TriggerServerEvent('esx_blanchiment:confiscatePlayerItem', GetPlayerServerId(player), itemType, itemName, amount)



          OpenBodySearchMenu(player)



        end



      end,

      function(data, menu)

        menu.close()

      end

    )



  end, GetPlayerServerId(player))



end



